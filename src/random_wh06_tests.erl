-module(random_wh06_tests).

-export([dump/0]).

%% @doc dumping reference output for the regression test

dump_rec1(0, Acc) ->
     lists:reverse(Acc);
dump_rec1(I, Acc) ->
     V = random_wh06:uniform(),
     dump_rec1(I - 1, [{V, get(random_wh06_seed)}| Acc]).

dump() ->
    put(random_wh06_seed, {123456789, 345678901, 567890123, 789012345}),
    io:format("~p~n", [dump_rec1(1000, [])]),
    ok.

%% EUnit test functions

-ifdef(TEST).

-include_lib("eunit/include/eunit.hrl").

%% @doc random_wh06:uniform() value loop (without output)

uniform_test1_rec1(0, Acc) ->
     lists:reverse(Acc);
uniform_test1_rec1(I, Acc) ->
     random_wh06:uniform(),
     uniform_test1_rec1(I - 1, [get(random_wh06_seed)| Acc]).

%% @doc random_wh06:uniform() value loop (with output)

uniform_test2_rec1(0, Acc) ->
     lists:reverse(Acc);
uniform_test2_rec1(I, Acc) ->
     V = random_wh06:uniform(),
     uniform_test2_rec1(I - 1, [{V, get(random_wh06_seed)}| Acc]).

%% @doc random_wh06:uniform_s() value loop (with output and state)

uniform_s_test3_rec1(0, Acc, _) ->
     lists:reverse(Acc);
uniform_s_test3_rec1(I, Acc, RS) ->
     {V, RS2} = random_wh06:uniform_s(RS),
     uniform_s_test3_rec1(I - 1, [{V, RS2} | Acc], RS2).

%% @doc Value tests of the first 1000 random numbers 
%% Note: internal state only

value_tests_1() ->
    Reflist = test_refval1(),
    put(random_wh06_seed, {123456789, 345678901, 567890123, 789012345}),
    Out1 = uniform_test1_rec1(1000, []),
    ?assertEqual(Reflist, Out1).

%% @doc Value tests of the first 1000 random numbers 
%% including float output

value_tests_2() ->
    Reffloat = test_refval2(),
    put(random_wh06_seed, {123456789, 345678901, 567890123, 789012345}),
    Out2 = uniform_test2_rec1(1000, []),
    ?assertEqual(Reffloat, Out2).

%% @doc Value tests of the first 1000 random numbers 
%% with explicit state

value_tests_3() ->
    Reffloat = test_refval2(),
    Out3 = uniform_s_test3_rec1(1000, [], {123456789, 345678901, 567890123, 789012345}),
    ?assertEqual(Reffloat, Out3).

%% @doc simple testing function as used in EUnit

simple_test_() -> 
    [
     ?_assertMatch(ok, value_tests_1()),
     ?_assertMatch(ok, value_tests_2()),
     ?_assertMatch(ok, value_tests_3())
    ].

